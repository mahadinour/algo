#include<stdio.h>

int main(int argc, char const *argv[])
{
    int g[200][200] ,n;
    printf("enter adj length");
    scanf("%d",&n);
    for(int i = 0 ; i < n ; i++){
        for(int j = 0 ; j< n; j++){
            g[i][j];
        }
    }

    for(int i = 0 ; i < n ; i++){
        for(int j = 0 ; j< n; j++){
            int x;
            g[i][j] = x;
            printf("%d",x);
        }
    }


    return 0;
}
